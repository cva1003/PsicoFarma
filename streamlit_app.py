    pdf.cell(0, 10, f"Consumo de alcohol: {'S√≠' if alcohol == 'S√≠' else 'No'}", ln=True)
    pdf.cell(0, 10, f"Fuma o ha fumado: {'S√≠' if fumador == 'S√≠' else 'No'}", ln=True)
    pdf.ln(10)

    # Historial m√©dico
    pdf.set_font("Arial", style="B", size=14)
    pdf.cell(0, 10, "3. Historial M√©dico", ln=True, align="L")
    pdf.set_font("Arial", size=12)
    pdf.ln(5)
    if enfermedades:
        pdf.cell(0, 10, "Enfermedades preexistentes:", ln=True)
        for enfermedad in enfermedades:
            pdf.cell(0, 10, f"- {enfermedad}", ln=True)
    else:
        pdf.cell(0, 10, "No se reportan enfermedades preexistentes.", ln=True)
    pdf.ln(10)


    # Informaci√≥n gen√©tica y m√©dica
    pdf.set_font("Arial", style="B", size=14)
    pdf.cell(0, 10, "4. Informaci√≥n Gen√©tica y M√©dica", ln=True, align="L")
    pdf.set_font("Arial", size=12)
    pdf.ln(5)
    pdf.cell(0, 10, f"Gen evaluado: {gen}", ln=True)
    pdf.cell(0, 10, f"Medicamento evaluado: {medicamento}", ln=True)
    pdf.cell(0, 10, f"Alelo identificado: {alelo}", ln=True)
    pdf.ln(10)

    # Recomendaciones
    pdf.set_font("Arial", style="B", size=14)
    pdf.set_text_color(220, 53, 69)  # Rojo profesional
    pdf.cell(0, 10, "5. Recomendaci√≥n Personalizada", ln=True, align="L")
    pdf.set_font("Arial", size=12)
    pdf.set_text_color(0, 0, 0)
    pdf.ln(5)
    pdf.multi_cell(0, 10, recomendacion, align="J")
    pdf.ln(10)

    # L√≠nea final para cerrar el informe
    pdf.set_draw_color(169, 169, 169)  # Gris claro
    pdf.set_line_width(0.5)
    pdf.line(15, pdf.get_y(), 195, pdf.get_y())
    pdf.ln(10)

    # Salida del PDF
    pdf_output = BytesIO()
    pdf_output.write(pdf.output(dest="S"))
    pdf_output.seek(0)
    return pdf_output

